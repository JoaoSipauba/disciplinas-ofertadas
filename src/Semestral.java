public class Semestral extends Presencial{

    public Semestral(int ano, int semestre, int turma, String diaSemana, int hora, int duracao){
        this.ano = ano;
        this.semestre = semestre;
        this.turma = turma;
        this.diaSemana = diaSemana;
        this.hora = hora;
        this.duracao = duracao;
        this.modalidade = "S";
    }

}
